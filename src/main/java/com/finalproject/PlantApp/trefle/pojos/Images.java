package com.finalproject.PlantApp.trefle.pojos;

public class Images {
	
	private String url;

	public Images() {
		super();
	}

	public Images(String url) {
		super();
		this.url = url;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	@Override
	public String toString() {
		return "Images [url=" + url + "]";
	}
	
	

}
